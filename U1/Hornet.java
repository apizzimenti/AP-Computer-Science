/* This project will display a hornet
   using escape characters.
*/

public class Hornet
{
	public static void main(String[] args)
	{
		System.out.println("\\ /");     // antenna
		System.out.print(" O(|||)>\n"); // body
		System.out.println("   \" \""); // feett
	}
}

